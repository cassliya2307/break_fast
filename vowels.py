string = "TAYELOLU"
count = 0

for letters in string:
	if letters == "a" or letters == "A":
		count += 1

	elif letters == "e" or letters == "E":
		count += 1
	
	elif letters == "i" or letters == "I":
		count += 1

	elif letters == "o" or letters == "O":
		count += 1

	elif letters == "u" or letters == "U":
		count += 1

print(count)